                            <p>
            Lo más importante a nivel de distribución de la
            alimentación durante el día es jamás omitir alguna
            de las comidas. Una persona en miras a bajar de
            peso, o mantenerlo, debe comer hasta 5 veces al día:
            desayunar, merendar a media mañana, almorzar,
            merendar a media tarde y cenar.</p>
            <p>
            Estas meriendas son sumamente importantes para
            no llegar a la próxima comida con demasiada
            hambre. Al comer poco pero frecuente mantenemos
            el apetito bajo control y de esa forma llegamos a la
            próxima comida con más capacidad para controlar
            las cantidades. Sin embargo estas meriendas no
            deben ser “otro almuerzo”. Las mismas pueden
